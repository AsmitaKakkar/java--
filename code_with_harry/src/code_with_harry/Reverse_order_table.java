package code_with_harry;

import java.util.Scanner;

public class Reverse_order_table {
	
	public static void main(String [] args)
	{
		System.out.println("***************Table of 10 in reverse order*************");
		for(int i=10;i>0;i--)
		{
			System.out.println("10 * "+i+" = "+10*i);
		}
		
	}
}
